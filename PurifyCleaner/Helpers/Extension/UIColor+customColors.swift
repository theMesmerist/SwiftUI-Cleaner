import UIKit

extension UIColor {
    func backgroundColor() -> UIColor {
        return UIColor(named: "backgroundColor")!
    }
    
    func mainColorFirst() -> UIColor {
        return UIColor(named: "mainColorFirst")!
    }
    
    func mainColorSecond() -> UIColor {
        return UIColor(named: "mainColorSecond")!
    }
    
    func textColorFirst() -> UIColor {
        return UIColor(named: "textColorFirst")!
    }
    
    func bgFloat() -> UIColor {
        return UIColor(named: "bgFloat")!
    }
}
